
package javaprograms;

public class ControlFlow {
    
    public static void main(String[] args) {
        
        int today = 4; 
        String day = "";


        switch (today) {
            case 1: day = "Monday";
                break;
            case 2: day = "Tuesday";
                break;
            case 3: day = "Wednesday";
                break;
            case 4: day = "Thrusday";
                break;
            case 5: day = "Friday";
                break;
            case 6: day = "Saterday";
                break;    
            case 7: day = "Sunday";
                break; 
                
            default: day = "Noday";
               
        }
        

        
        
        
    }
    
}
