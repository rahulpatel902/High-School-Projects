    private void b3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b3ActionPerformed

        double percentage  = Double.parseDouble(t9.getText());

        if(percentage >= 95){
            String grade = "A+";
            t10.setText("" + grade);
        }
        else if (percentage >= 85){
            String grade = "A";
            t10.setText("" + grade);
        }        
        else if (percentage >= 65){
            String grade = "B";
            t10.setText("" + grade);
        }
        else if (percentage >= 33){
            String grade = "C";
            t10.setText("" + grade);
        }
        else{
            String grade = "D";
            t10.setText("" + grade);
        }

        
        
        
        String grade1 = t10.getText();

        if ("A+".equals(grade1)){

            t11.setText("Pass");
        }
        else if ("A".equals(grade1)){
            t11.setText("Pass");
        }
        else if ("B".equals(grade1)){
            t11.setText("Pass");
        }        
        else if ("C".equals(grade1)){
            t11.setText("Pass");
        }
        else {
            t11.setText("Fail");
        }

    }//GEN-LAST:event_b3ActionPerformed

